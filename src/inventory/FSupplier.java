    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        String code = this.txtKodeSupplier.getText();

        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data dengan kode " + code + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection c = InventoryDB.getKoneksi();
                String sql = "DELETE FROM suppliers WHERE code = ?";

                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, code);
                p.executeUpdate();
                p.close();

                JOptionPane.showMessageDialog(this, "Berhasil menghapus data dengan kode " + code);
            } catch (SQLException e) {
                System.err.println(e);
                JOptionPane.showMessageDialog(this, "Gagal menghapus data");
            } finally {
                loadDatabase();
                bersih();
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String code = this.txtKodeSupplier.getText(); // Gets the value from the JTextField for the supplier code
        String name = this.txtNamaSupplier.getText(); // Gets the value from the JTextField for the supplier name

        if (code.isEmpty() || name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kode supplier dan nama supplier wajib diisi");
        } else {
            try {
                Connection c = InventoryDB.getKoneksi();
                String checkIfExistsQuery = "SELECT COUNT(*) FROM suppliers WHERE code = ?";
                PreparedStatement checkIfExistsStmt = c.prepareStatement(checkIfExistsQuery);
                checkIfExistsStmt.setString(1, code);
                ResultSet rs = checkIfExistsStmt.executeQuery();

                if (rs.next() && rs.getInt(1) > 0) {
                    int confirmUpdate = JOptionPane.showConfirmDialog(this, "Apakah Anda ingin melakukan update supplier dengan kode " + code + "?", "Konfirmasi Update", JOptionPane.YES_NO_OPTION);

                    if (confirmUpdate == JOptionPane.YES_OPTION) {
                        // If the user confirms the update, perform the update operation
                        String updateQuery = "UPDATE suppliers SET name = ? WHERE code = ?";
                        PreparedStatement updateStmt = c.prepareStatement(updateQuery);
                        updateStmt.setString(1, name);
                        updateStmt.setString(2, code);
                        updateStmt.executeUpdate();

                        JOptionPane.showMessageDialog(this, "Berhasil diubah");
                    } else {
                        JOptionPane.showMessageDialog(this, "Update dibatalkan");
                    }
                } else {
                    // If the supplier code does not yet exist, perform an insert operation
                    String insertQuery = "INSERT INTO suppliers (code, name) VALUES (?, ?)";
                    PreparedStatement insertStmt = c.prepareStatement(insertQuery);
                    insertStmt.setString(1, code);
                    insertStmt.setString(2, name);
                    insertStmt.executeUpdate();

                    JOptionPane.showMessageDialog(this, "Berhasil ditambahkan");
                }

                // Clean and update the display
                loadDatabase();
                bersih();
            } catch (SQLException e) {
                System.err.println(e);
                JOptionPane.showMessageDialog(this, "Gagal");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

