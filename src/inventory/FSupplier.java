    private void tblSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSupplierMouseClicked
        int row = tblSupplier.getSelectedRow();

        if (row != -1) {
            try {
                String code = tblSupplier.getValueAt(row, 0).toString(); // Retrieve the supplier code from the first column (index 0)
                String name = tblSupplier.getValueAt(row, 1).toString(); // Retrieve the supplier name from the second column (index 1)

                // Set values to UI components based on the retrieved data
                txtKodeSupplier.setText(code);
                txtNamaSupplier.setText(name);

                JOptionPane.showMessageDialog(this, "Data berhasil ditampilkan");
            } catch (Exception e) {
                System.err.println(e);
                JOptionPane.showMessageDialog(this, "Gagal menampilkan data");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Silakan pilih baris terlebih dahulu");
        }
    }//GEN-LAST:event_tblSupplierMouseClicked

